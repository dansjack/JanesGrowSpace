        <h2 class="pageID">Responsive (website) v.s. Native (mobile application) </h2>
        <p>
          This page explores the differences between responsive websites and mobile applications. First, some
          definitions: a mobile app refers to software that you download to your phone from your platform’s app
          store (iOS, Android, etc) while a responsive website refers to a site that is optimized for mobile
          viewports. Over the next few paragraphs we’ll cover the pros and cons of responsive design and mobile
          apps, then finish with some questions to ask when considering which development approach is appropriate.
        </p>
        <p>
          While making responsive websites for your product is the older of these two techniques, there are still
          benefits unique to this approach. Britt Armour of Clearbridge Mobile lists three pros: a broad audience
          reach, cost effectiveness, and SEO (Search Engine Optimization). Armour explains that because responsive
          sites aren’t built for a specific mobile platform, you make your product immediately available across all
          mobile platforms. This ties in with Armour’s second point, responsive websites are cheaper than building
          mobile apps when you want to cater to more than one platform (Armour).
        </p>
        <p>
          Tania H of RubyGarage makes another point in favor of responsive design cost effectiveness. With
          responsive sites, “...you change the content or design just by editing them once, and allow you to do that
          efficiently and flexibly...updates become active and visible immediately across all types of devices.”
          (Tania) Regarding SEO, Armour writes “Mobile optimized sites are now ranked higher in search engine
          results pages than sites that are not optimized for mobile...which results in greater brand visibility and
          higher website traffic.” (Armour)
        </p>
        <p>
          The convenience and cost savings of only needing one version of your responsive site may be offset by some
          of the drawbacks. Tania H explains that responsive sites can’t effectively use all the features of a
          smartphone like a mobile app can. She writes, “Cameras, GPS, phone dialing, and other features integrated
          into mobile devices aren't always well developed for responsive/mobile websites...” (Tania) Another oft
          cited drawback for responsive sites is that you need a solid internet connection for them to operate
          effectively (Tania). Even if the connection is solid you may not find your audience as Pietro Saccomani of
          MobiLoud writes,
        </p>
        <blockquote cite="https://www.mobiloud.com/blog/mobile-apps-vs-the-mobile-web/">
          According to ComScore mobile apps dominate time spent vs. the mobile web... Looking at their numbers for
          US consumers from June 2017, mobile apps are leading at 87% of mobile time, with web browsing only scoring
          the remaining 13%.” (Saccomani)
        </blockquote>
        <p>
          In many cases, mobile apps have the inverse benefits and drawbacks of responsive websites. Per a
          ThinkMobiles article, mobile apps can work offline, allowing your customers more opportunity to interact
          with your product; mobile apps more effectively interact with the hardware in your phone (GPS, camera,
          mic, etc); and as mentioned earlier, people already spend most of their time on a mobile device on mobile
          apps (ThinkMobiles). Nitin Deshdeep of VWO Blog names some additional benefits of mobile apps including
          the ability to push notifications directly to your customers; your customers can personalize your app
          through preferences (customizable settings); and you are less restricted when designing your app. Deshdeep
          writes, “Mobile websites depend on browser features like ‘back button,’ ‘refresh button,’ and ‘address
          bar’ to work. Mobile Apps don’t have any of these restrictions.” (Deshdeep)
        </p>
        <p>
          On the other hand, mobile apps are more expensive to develop--especially if you plan to serve customers on
          more than one platform. They are also more expensive to upgrade and patch. IT consultants Cleveroad
          mention that “Digital products almost always contain small issues which may appear through some time after
          release…Even if you have no bugs, you have to constantly advance your app and make it better... Otherwise,
          users will find a better option.” (Cleveroad)
        </p>
        <p>
          So how do you know whether it’s appropriate to make a mobile app, a responsive website, or both? Dmitry
          Garbar of BelitSoft lists some questions you should keep in mind when making that decision:
        </p>
        <ul>
          <li>What time and resources do you have for the project?</li>
          <li>How often will you need or want to update the project?</li>
          <li>Do you need offline functionality?</li>
          <li>Do you need to take advantage of mobile device hardware (GPS, camera, etc)?</li>
          <li>What audience are you looking for?</li>
          <li>What industry does your product belong to? (Garbar)</li>
        </ul>
        <p>
          Since reviewing the benefits and drawbacks of responsive websites and mobile apps, some of the answers may
          seem obvious. If you think your time and/or resources are a concern or if your app needs to be updated
          often, consider building a responsive website. It will be cheaper--in time and money--to implement and
          update. If your product needs to be accessed with or without internet, you should probably go with a
          mobile app. The same applies if your product needs access to native phone apps like the camera, GPS, or
          the microphone.
        </p>
        <p>
          Although responsive websites are platform neutral and great for SEO, they do have limitations with respect
          to keeping your audience. Garbar writes, “...mobile websites are a good choice for establishing initial
          awareness and engagement. However, for more personalized customer-user interactions and retention, it is
          better to go with an app.” (Garbar) So, whether you choose mobile app or responsive site may also depend
          on how established your company is. Are you a well established company with brand recognition? You
          probably want an app designed to retain customers. Are you a start-up looking to promote their brand?
          Consider building a responsive website that will get your product exposed to a broad audience.
        </p>
        <p>
          What if you need the audience benefits of both an app and a responsive site? You may need to develop both.
          Tom Farrell of Swrve writes, “If you rely on discovery via social channels and you feel downloading the
          app is too much for many casual visitors, you need both. [For example] ...media companies, financial
          institutions, travel companies and retailers.” (Farrell)
        </p>
        <p>
          What business you are in is also important when considering which development path to choose. According to
          a report by the Interactive Advertising Bureau from 2017, “Search, purchasing and booking products or
          services are dominant mobile web functions. Mobile apps are preferred for entertainment. Mobile apps are
          the overwhelming choice for communications.” (IAB) With answers to the above questions along with the
          costs and benefits of apps and responsive sites, you’ll be able to make an informed decision about which
          development option is right for your product.
        </p>

