    <section>
        <h2 class="pageID">Shopping Carts</h2>
        <p>
          If you’re looking to sell your goods or services online, you’re going to need a shopping cart. If you
          haven't already built a website, you can take advantage of the website builder tools that many ecommerce
          software companies offer along with shopping cart buttons (e.g. Shopify). But if you already have an
          established site, you just need a shopping cart application that places a button on your site and lets the
          user buy your products.
        </p>
        <p>
          There are a few different tiers of shopping cart software out there. According to PayPal’s web page about
          online carts, there are: easy, non-technical options; customized solutions; and commercial solutions
          (PayPal). Non-technical options are great if you want to test demand for your product or can't afford some
          of the more robust options, as many apps in this group offer free and affordable plans (PayPal). If you
          don’t want to pay anything and want a lot of control over the app, then a customized solution is what
          you’re looking for. For example, the introduction on Zen Cart’s website reads, “Zen Cart&reg; truly is the art
          of e-commerce; free, user-friendly, open source shopping cart software.” (Zen Cart) Although these
          solutions are cheap and highly customizable, they can be difficult to work with. Regarding Zen Cart, Liz
          Hull of Merchant Maverick wrote their, “...outdated user interface and themes give me pause...the lack of
          support and usability suggests that Zen Cart is not the best option for most merchants.” (Hull)
        </p>
        <p>
          The last tier, commercial solutions, is the most complete. According to PayPal, these solutions provide
          around the clock customer support and CRM tools (PayPal). But unless you want your entire online business
          run from an ecommerce application, these options are unnecessary. Instead, you’re better off with a
          non-technical option. For example, Ecwid and Snipcart.
        </p>
        <p>
          Ecwid is a great choice for small businesses that are just getting started with their online vending.
          There is a free plan that allows you to sell up to ten items, and none of the plans charge a transaction
          fee, just fixed monthly fees. Their website offers lots of documentation on how to add your cart to your
          platform (e.g. Wordpress) and customize your app. It even includes a website builder if you decide you
          want to redo it. You can read more about Ecwid here: <a href="https://www.ecwid.com/pricing">
            https://www.ecwid.com/pricing</a>.
        </p>
        <p>
          Although Ecwid is pretty basic, Snipcart is even simpler. The product is just the shopping cart plugin.
          There are only the “standard” and the “I am special” plans. The standard plan charges a 2% transaction fee
          (or $10 if total sales per month are less than $500) and includes plenty of features like tech support,
          unlimited products, and customizable invoices. See a full feature list here:
          <a href="https://snipcart.com/pricing"> https://snipcart.com/pricing</a>. The “I am special” plan has the
          same features, but the pricing is negotiable based on the unique properties of your business.
        </p>
        <p>
          Either of these tools will get your online store operating quickly. If you think your business may
          eventually require a holistic ecommerce solution, you should start with Ecwid (or even consider some of
          the commercial solutions like Shopify). However, If you are strictly looking for a shopping cart plugin
          and plan to handle all future business through your independent website, Snipcart is a great option.
        </p>
