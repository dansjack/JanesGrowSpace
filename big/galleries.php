    <section>
        <h2 class="pageID">Galleries</h2>
        <p>
          We use galleries when we have image-based content that we want to give structure. There are many different
          gallery options out there ranging from simple HTML and CSS based galleries to jquery and javascript based
          galleries. In this paper we will explore some of the different options available.
        </p>
        <p>
          The W3Schools website provides some simple examples of how to create an image gallery just with HTML and
          CSS, or how to create a slightly more advanced gallery with a few lines of javascript. The W3Schools’
          first example, a simple (or responsive) image gallery, is made with a series of div elements and some CSS
          styles to create uniform spacing (and media queries, if being made responsive). It allows you to click on
          an image in the gallery to take you to a separate page with that image, and it provides a space for an
          image description (W3 Image Gallery) The W3Schools’ more robust “tab gallery” allows you to click on an
          image in the gallery and have it show up enlarged below the gallery. The enlarged image has its alt
          attribute value displayed in the bottom left of the image (W3 Tab Gallery).
        </p>
        <p>
          If these simple examples aren't enough for your site, there are plenty of other image galleries that offer
          more features. Websites provide consistent roundups that list some of the best options (in their opinion)
          for these feature-heavy image galleries. In an image gallery roundup for Hongkiat, Ashutosh KS highlights
          the Jssor Slider which provides hundreds of styles for not just images, but other content too. These
          sliders can be made with or without jquery. Ashutosh writes that Jssor “...is a compelling content and
          image slider with 300+ transition effects. ...it provides lots of demos and examples, which will help
          anyone to get up and running quickly with this slider solution.” (Ashutosh) Indeed, the demos that Jssor
          provides are very helpful and even if you don't end up using one of their templates, you now have a list
          of gallery-specific terms and examples to show you exactly what different kinds of transitions are
          possible (e.g. tab slider, banner slider, or simple fade slideshow).
        </p>
        <p>
          An elegant option presented by Ashutosh is the jQuery Gridder plugin, which “...displays a thumbnail grid
          of images with a beautiful expanding image preview, similar to Google Images.” (Ashutosh) However, some
          coding will be necessary to personalize the gallery. If that sounds too difficult, there are other gallery
          options that don't require coding, such as cssSlider, a free tool which bills itself as highly
          customizable and mobile friendly. The cssSlider home page summarizes its product: “Create beautiful image
          sliders entirely in CSS. No jQuery, no JavaScript, no image icons, no coding! It's amazingly fast, light,
          responsive, and retina-ready. Works on all devices and browsers.” (cssSlider) If you can't find a gallery
          you like on cssSlider, there are plenty of other code-free options like<a href="https://www.juicebox.net/">
            Juicebox</a>.
        </p>
        <p>
          With so many options to choose from, it may be hard to make a decision. But if you stop and consider what
          you’re going to use a gallery for, you can begin to narrow down which option is right for you. If you need
          an image gallery to showcase a series of products, then you probably want a gallery that can provide
          plenty of additional information.<a href="http://oriongunning.github.io/gridder/"> The jQuery Gridder
            plugin</a> would be a good choice for this purpose. If you’re looking to showcase art or photography, then
          <a href="http://cssslider.com/demos.html">one of the cssSlider templates</a> is probably right for the job. If
          your gallery is for a slideshow roundup or for product/business related photographs that don’t need much
          context, you can probably find what you need on the
          <a href="https://www.jssor.com/demos/image-slider.slider">Jssor Slider website</a>.
        </p>
